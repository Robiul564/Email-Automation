"""
Custom Evaluation Metrics for Email Generation
Implements 3 tailored metrics for assessing email quality
"""

import re
from typing import List, Dict
from openai import OpenAI
import os


class EmailEvaluator:
    """
    Evaluates generated emails using 3 custom metrics:
    1. Fact Incorporation Score: Measures how well all key facts are included
    2. Tone Adherence Score: Measures how well the tone matches the intent
    3. Professional Quality Score: Measures clarity, structure, and grammar
    """
    
    def __init__(self, api_key=None, model="gpt-3.5-turbo"):
        """Initialize the evaluator with OpenAI client."""
        api_key = api_key or os.getenv("OPENAI_API_KEY")
        self.model = model

        if api_key:
            self.client = OpenAI(api_key=api_key)
            self.offline = False
        else:
            # Use lightweight heuristic judges when no API key is provided
            self.client = None
            self.offline = True
    
    # ==================== METRIC 1: FACT INCORPORATION SCORE ====================
    def metric_1_fact_incorporation(
        self,
        generated_email: str,
        key_facts: List[str],
        reference_email: str = None
    ) -> Dict[str, float]:
        """
        METRIC 1: Fact Incorporation Score (0-100)
        
        Measures how well all required key facts are seamlessly incorporated into the email.
        
        Scoring Logic:
        - Extracts core concepts from each key fact
        - Checks if these concepts appear in the generated email
        - Verifies natural integration (not just keyword matching)
        - Uses LLM as judge for semantic matching
        
        Focus: Specificity, Information Completeness, Fact Recall
        
        Args:
            generated_email: The email generated by the assistant
            key_facts: List of key facts that should be included
            reference_email: Optional reference email for comparison
        
        Returns:
            Dict with 'score', 'details', 'fact_coverage'
        """
        prompt = f"""You are an expert evaluator assessing whether key facts are properly incorporated into an email.

Generated Email:
{generated_email}

Key Facts Required:
{chr(10).join([f'- {fact}' for fact in key_facts])}

Evaluate whether EACH key fact is present and naturally integrated in the email.
For each key fact, assign a score (0-100 where):
  100 = Fact clearly present and naturally integrated
  70 = Fact present but could be more naturally integrated
  40 = Fact partially present or tangentially mentioned
  0 = Fact completely missing

Also provide:
1. Overall Fact Incorporation Score (average of all facts)
2. Which facts are missing or poorly integrated
3. Brief explanation of how facts were incorporated

Format your response as:
OVERALL_SCORE: [number]
INDIVIDUAL_SCORES: [fact 1 score], [fact 2 score], ... (comma-separated)
MISSING_FACTS: [list or "None"]
EXPLANATION: [brief explanation]"""

        if self.offline:
            # Simple keyword-based fact check
            individual_scores = []
            missing = []
            for fact in key_facts:
                tokens = re.findall(r"\w+", fact.lower())
                present = sum(1 for t in tokens if t in generated_email.lower())
                frac = present / max(1, len(tokens))
                score = round(100 * frac)
                individual_scores.append(score)
                if score < 50:
                    missing.append(fact)

            overall_score = sum(individual_scores) / len(individual_scores) if individual_scores else 0
            fact_coverage = sum(1 for s in individual_scores if s >= 50) / len(individual_scores) * 100 if individual_scores else 0

            return {
                "score": overall_score,
                "individual_fact_scores": individual_scores,
                "fact_coverage_percent": fact_coverage,
                "missing_facts": missing if missing else None,
                "explanation": "Keyword-based fact matching (offline heuristic)",
                "metric_name": "Fact Incorporation Score",
                "focus": "Specificity, Information Completeness, Fact Recall"
            }

        response = self.client.chat.completions.create(
            model=self.model,
            messages=[
                {"role": "system", "content": "You are a strict evaluator of email quality."},
                {"role": "user", "content": prompt}
            ],
            temperature=0.3,
            max_tokens=500
        )
        
        result_text = response.choices[0].message.content.strip()
        
        # Parse the response
        overall_score = self._extract_score(result_text, "OVERALL_SCORE")
        individual_scores = self._extract_list(result_text, "INDIVIDUAL_SCORES")
        missing_facts = self._extract_section(result_text, "MISSING_FACTS")
        explanation = self._extract_section(result_text, "EXPLANATION")
        
        fact_coverage = len(individual_scores) / len(key_facts) * 100 if key_facts else 0
        
        return {
            "score": overall_score,
            "individual_fact_scores": individual_scores,
            "fact_coverage_percent": fact_coverage,
            "missing_facts": missing_facts,
            "explanation": explanation,
            "metric_name": "Fact Incorporation Score",
            "focus": "Specificity, Information Completeness, Fact Recall"
        }
    
    # ==================== METRIC 2: TONE ADHERENCE SCORE ====================
    def metric_2_tone_adherence(
        self,
        generated_email: str,
        intended_tone: str,
        reference_email: str = None
    ) -> Dict[str, float]:
        """
        METRIC 2: Tone Adherence Score (0-100)
        
        Measures how well the generated email matches the specified tone.
        
        Scoring Logic:
        - Analyzes linguistic patterns (formality markers, sentence structure)
        - Compares emotional register to intended tone
        - Uses LLM to semantically judge tone alignment
        - Checks for consistency throughout the email
        
        Focus: Tone Accuracy, Format Adherence, Consistency
        
        Args:
            generated_email: The email generated by the assistant
            intended_tone: The desired tone (e.g., "formal", "casual", "urgent")
            reference_email: Optional reference email for comparison
        
        Returns:
            Dict with 'score', 'tone_analysis', 'consistency_rating'
        """
        prompt = f"""You are an expert in communication style and tone analysis.

Evaluate whether this email matches the specified tone perfectly.

Intended Tone: {intended_tone}

Generated Email:
{generated_email}

Assess the following dimensions:
1. Formality Level (0-100): Does the formality match the intended tone?
2. Emotional Register (0-100): Are emotions/urgency appropriate?
3. Consistency (0-100): Is the tone consistent throughout the email?
4. Language Choices (0-100): Are vocabulary and expressions fitting?
5. Sentence Structure (0-100): Is sentence complexity appropriate for the tone?

Then provide:
- Overall Tone Adherence Score (average of above 5 dimensions)
- Key tone strengths
- Areas where tone could be improved

Format your response as:
FORMALITY_LEVEL: [number]
EMOTIONAL_REGISTER: [number]
CONSISTENCY: [number]
LANGUAGE_CHOICES: [number]
SENTENCE_STRUCTURE: [number]
OVERALL_SCORE: [number]
STRENGTHS: [brief description]
IMPROVEMENTS: [brief description]"""

        if self.offline:
            # Simple tone heuristics based on keyword presence
            tone = intended_tone.lower()
            scores = {
                "formality": 50,
                "emotional": 50,
                "consistency": 80,
                "language": 60,
                "structure": 70
            }

            # Adjust heuristics for common tones
            if "formal" in tone or "professional" in tone:
                scores.update({"formality": 90, "language": 80})
            if "casual" in tone:
                scores.update({"formality": 30, "language": 60})
            if "urgent" in tone:
                scores.update({"emotional": 90, "consistency": 90})
            if "empathetic" in tone or "support" in tone:
                scores.update({"emotional": 85, "language": 75})

            overall = sum(scores.values()) / len(scores)

            return {
                "score": overall,
                "formality_level": scores["formality"],
                "emotional_register": scores["emotional"],
                "consistency": scores["consistency"],
                "language_choices": scores["language"],
                "sentence_structure": scores["structure"],
                "strengths": "Heuristic tone-match (offline)",
                "improvements": "Use LLM judge for finer analysis",
                "metric_name": "Tone Adherence Score",
                "focus": "Tone Accuracy, Format Adherence, Consistency"
            }

        response = self.client.chat.completions.create(
            model=self.model,
            messages=[
                {"role": "system", "content": "You are a strict evaluator of communication tone."},
                {"role": "user", "content": prompt}
            ],
            temperature=0.3,
            max_tokens=500
        )
        
        result_text = response.choices[0].message.content.strip()
        
        # Parse the response
        overall_score = self._extract_score(result_text, "OVERALL_SCORE")
        formality = self._extract_score(result_text, "FORMALITY_LEVEL")
        emotional = self._extract_score(result_text, "EMOTIONAL_REGISTER")
        consistency = self._extract_score(result_text, "CONSISTENCY")
        language = self._extract_score(result_text, "LANGUAGE_CHOICES")
        structure = self._extract_score(result_text, "SENTENCE_STRUCTURE")
        strengths = self._extract_section(result_text, "STRENGTHS")
        improvements = self._extract_section(result_text, "IMPROVEMENTS")
        
        return {
            "score": overall_score,
            "formality_level": formality,
            "emotional_register": emotional,
            "consistency": consistency,
            "language_choices": language,
            "sentence_structure": structure,
            "strengths": strengths,
            "improvements": improvements,
            "metric_name": "Tone Adherence Score",
            "focus": "Tone Accuracy, Format Adherence, Consistency"
        }
    
    # ==================== METRIC 3: PROFESSIONAL QUALITY SCORE ====================
    def metric_3_professional_quality(
        self,
        generated_email: str,
        reference_email: str = None
    ) -> Dict[str, float]:
        """
        METRIC 3: Professional Quality Score (0-100)
        
        Measures overall professional quality: clarity, structure, grammar, and effectiveness.
        
        Scoring Logic:
        - Analyzes email structure (opening, body, closing)
        - Checks grammar, spelling, and fluency
        - Evaluates clarity and conciseness
        - Assesses call-to-action effectiveness
        - Uses LLM to judge professional appropriateness
        
        Focus: Grammar/Fluency, Introduction Effectiveness, Conciseness
        
        Args:
            generated_email: The email generated by the assistant
            reference_email: Optional reference email for comparison
        
        Returns:
            Dict with 'score', 'quality_aspects', 'recommendations'
        """
        prompt = f"""You are an expert in professional communication and business writing.

Evaluate the overall professional quality of this email.

Generated Email:
{generated_email}

Assess the following dimensions:
1. Grammar & Spelling (0-100): Is the email grammatically correct with proper spelling?
2. Clarity (0-100): Is the message clear and easy to understand?
3. Conciseness (0-100): Is the email appropriately concise without being abrupt?
4. Structure (0-100): Does the email have clear opening, body, and closing?
5. Call-to-Action (0-100): Is there a clear next step or call-to-action?
6. Professionalism (0-100): Does the email maintain appropriate professionalism?
7. Opening Effectiveness (0-100): Does the opening engage appropriately?

Then provide:
- Overall Professional Quality Score (average of above 7 dimensions)
- Email length assessment (word count)
- Recommendations for improvement

Format your response as:
GRAMMAR_SPELLING: [number]
CLARITY: [number]
CONCISENESS: [number]
STRUCTURE: [number]
CALL_TO_ACTION: [number]
PROFESSIONALISM: [number]
OPENING_EFFECTIVENESS: [number]
OVERALL_SCORE: [number]
WORD_COUNT: [number]
RECOMMENDATIONS: [brief recommendations or "None needed"]"""

        if self.offline:
            # Heuristic quality scoring
            sentences = re.split(r"[.!?]+", generated_email)
            sentences = [s.strip() for s in sentences if s.strip()]
            word_count = len(generated_email.split())

            grammar_score = 80 if re.search(r"\b\w+\b", generated_email) else 50
            clarity = max(40, 100 - abs(12 - (word_count / max(1, len(sentences)))))
            conciseness = max(30, 100 - (word_count / 2)) if word_count > 40 else 90
            structure = 80 if "Dear [Recipient]" in generated_email or "Dear" in generated_email else 60
            cta = 90 if re.search(r"(please|let me know|confirm|reply)", generated_email, re.I) else 40
            professionalism = (grammar_score + structure + clarity) / 3
            opening = 80 if re.search(r"Dear|Hi|Hello|To:", generated_email) else 50

            overall = sum([grammar_score, clarity, conciseness, structure, cta, professionalism, opening]) / 7

            return {
                "score": overall,
                "grammar_spelling": grammar_score,
                "clarity": clarity,
                "conciseness": conciseness,
                "structure": structure,
                "call_to_action": cta,
                "professionalism": professionalism,
                "opening_effectiveness": opening,
                "word_count": word_count,
                "recommendations": "Heuristic recommendations: consider LLM judge for precise feedback",
                "metric_name": "Professional Quality Score",
                "focus": "Grammar/Fluency, Introduction Effectiveness, Conciseness"
            }

        response = self.client.chat.completions.create(
            model=self.model,
            messages=[
                {"role": "system", "content": "You are a strict evaluator of professional writing."},
                {"role": "user", "content": prompt}
            ],
            temperature=0.3,
            max_tokens=500
        )
        
        result_text = response.choices[0].message.content.strip()
        
        # Parse the response
        overall_score = self._extract_score(result_text, "OVERALL_SCORE")
        grammar = self._extract_score(result_text, "GRAMMAR_SPELLING")
        clarity = self._extract_score(result_text, "CLARITY")
        conciseness = self._extract_score(result_text, "CONCISENESS")
        structure = self._extract_score(result_text, "STRUCTURE")
        cta = self._extract_score(result_text, "CALL_TO_ACTION")
        professionalism = self._extract_score(result_text, "PROFESSIONALISM")
        opening = self._extract_score(result_text, "OPENING_EFFECTIVENESS")
        word_count = self._extract_score(result_text, "WORD_COUNT")
        recommendations = self._extract_section(result_text, "RECOMMENDATIONS")
        
        # Also calculate actual word count
        actual_word_count = len(generated_email.split())
        
        return {
            "score": overall_score,
            "grammar_spelling": grammar,
            "clarity": clarity,
            "conciseness": conciseness,
            "structure": structure,
            "call_to_action": cta,
            "professionalism": professionalism,
            "opening_effectiveness": opening,
            "word_count": actual_word_count,
            "recommendations": recommendations,
            "metric_name": "Professional Quality Score",
            "focus": "Grammar/Fluency, Introduction Effectiveness, Conciseness"
        }
    
    def evaluate_email(
        self,
        generated_email: str,
        intent: str,
        key_facts: List[str],
        tone: str,
        reference_email: str = None
    ) -> Dict:
        """
        Evaluate an email across all 3 metrics.
        
        Args:
            generated_email: The email to evaluate
            intent: The intent of the email
            key_facts: The key facts that should be included
            tone: The intended tone
            reference_email: Optional reference for comparison
        
        Returns:
            Dict with all metric scores and analysis
        """
        metric_1 = self.metric_1_fact_incorporation(generated_email, key_facts, reference_email)
        metric_2 = self.metric_2_tone_adherence(generated_email, tone, reference_email)
        metric_3 = self.metric_3_professional_quality(generated_email, reference_email)
        
        # Calculate overall average
        overall_score = (metric_1["score"] + metric_2["score"] + metric_3["score"]) / 3
        
        return {
            "overall_score": overall_score,
            "metric_1_fact_incorporation": metric_1,
            "metric_2_tone_adherence": metric_2,
            "metric_3_professional_quality": metric_3,
            "evaluation_complete": True
        }
    
    # ==================== HELPER METHODS ====================
    def _extract_score(self, text: str, key: str) -> float:
        """Extract a numeric score from LLM response."""
        pattern = rf"{key}:\s*(\d+(?:\.\d+)?)"
        match = re.search(pattern, text, re.IGNORECASE)
        if match:
            return float(match.group(1))
        return 0.0
    
    def _extract_section(self, text: str, key: str) -> str:
        """Extract a text section from LLM response."""
        pattern = rf"{key}:\s*(.+?)(?=\n[A-Z_]+:|$)"
        match = re.search(pattern, text, re.IGNORECASE | re.DOTALL)
        if match:
            return match.group(1).strip()
        return ""
    
    def _extract_list(self, text: str, key: str) -> List[float]:
        """Extract a comma-separated list of scores."""
        pattern = rf"{key}:\s*(.+?)(?=\n[A-Z_]+:|$)"
        match = re.search(pattern, text, re.IGNORECASE | re.DOTALL)
        if match:
            values = match.group(1).strip().split(",")
            return [float(v.strip()) for v in values if v.strip()]
        return []
